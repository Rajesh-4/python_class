#Arithmetic operator
a=17
b=3
print("Addition",a+b)
print("Subtraction",a-b)
print("Multipication ",a*b)
print("Division",a/b)
print("Modulo",a%b)
print("Floor division is",a//b)
print("Exponent",a**b)

#Relational operator

print("a>b",a>b)
print("a<b",a<b)
print("a>=b",a>=b)
print("a<=b",a<=b)
print("a==b",a==b)
print("a!=b",a!=b)

#logical operator

print(a>b and a<b)
print(a>b and a!=b)
print(a>b or a<b)
print( not a<b)

